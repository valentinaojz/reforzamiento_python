#Ejecución condicional 
#Todos los programas hacen evaluaciones condicionales según el valor de sus variables y expreciones. Por lo tanto no todas las lineas de código se ejecutan cuando corre un programa.
#Para esto está el concepto de Expresión Booleana simples.
# Ej:
print(5 < 4) #False 
print(5 > 4) #True
print(4 != 4) #False !para comparar si es distinto de.
print(4 == 4) #True siempre para comparar si es igual es doble igual.

#Cuando queremos comparar menos o igual es operador es <= ej:
print(4 <= 6)
#El mayor igual es >= 
print(5 >= 3) 
#noexiste el => ni el =< 

#podemos tener expreciones booleanas más completas utilizando los operadores and, or y not 

print("- - - - Expreciones booleanas compuetas- - - -")
print(4 < 5 or 6 > 8) #True or False => True 
print(4 < 5 and 6 > 8) # True and false => false 
print(not True) #=> False 

 